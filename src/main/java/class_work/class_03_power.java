public class class_03_power {
    public static void main(String[] args) {
        double a = 4.4;
        double b;

        System.out.println("a number is " + a);
        b = a * a;
        System.out.println("a ** 4 is " + b * b);
        b = a * a * a;
        System.out.println("a ** 6 is " + b * b);
    }
}
